#include "libft.h"

char	*ft_strrchr(const char	*s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i--;
	}
	if ((unsigned char)s[0] == (char)c)
		return ((char *)s);
	return (NULL);
}
